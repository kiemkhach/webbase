package com.energisme.bean;

public class ConfigLab009Bean {
	private String energyName;
	private String energyCode;
	private Double coefficientCO2;
	public String getEnergyName() {
		return energyName;
	}
	public void setEnergyName(String energyName) {
		this.energyName = energyName;
	}
	public String getEnergyCode() {
		return energyCode;
	}
	public void setEnergyCode(String energyCode) {
		this.energyCode = energyCode;
	}
	public Double getCoefficientCO2() {
		return coefficientCO2;
	}
	public void setCoefficientCO2(Double coefficientCO2) {
		this.coefficientCO2 = coefficientCO2;
	}
	
	
}
